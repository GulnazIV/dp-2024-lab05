class DeliveryError(Exception):
    """
    Исключение, возникающее при отсутствии адреса доставки.
    """

    def __init__(self) -> None:
        """
        Инициализирует исключение DeliveryError.

        Устанавливает сообщение об ошибке, которое будет отображаться при возникновении этого исключения.
        """
        super().__init__("Отсутствует адрес доставки")

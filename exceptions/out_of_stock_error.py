class OutOfStockError(Exception):
    """
    Исключение, возникающее при отсутствии товара на складе.
    """

    def __init__(self) -> None:
        """
        Инициализирует исключение OutOfStockError.

        Устанавливает сообщение об ошибке, которое будет отображаться при возникновении этого исключения.
        """
        super().__init__("Отсутствует товар на складе")
